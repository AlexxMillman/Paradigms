a1 = input("Введіть число1 ")
a2 = input("Введіть число2 ")
a3 = input("Введіть число3 ")
print("%4.0f" % int(a1) + "%4.0f" % int(a2) + "%4.0f" % int(a3))
print('{0:4.0f}'.format(int(a1))+'{0:4.0f}'.format(int(a2))+'{0:4.0f}'.format(int(a3)))
print(f'{int(a1):4.0f}'+f'{int(a2):4.0f}'+f'{int(a3):4.0f}')

